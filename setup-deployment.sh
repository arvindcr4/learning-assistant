  NODE_ENV = "production"
  PORT = "3000"
  NEXT_TELEMETRY_DISABLED = "1"
EOF
        log_success "Created sample fly.toml configuration"
    else
        log_info "fly.toml already exists"
    fi
    
    # Create .deployignore file
    cat > "${SCRIPT_DIR}/.deployignore" << 'EOF'
# Files to ignore during deployment
